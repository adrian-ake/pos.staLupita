    private void btnPagarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarVentaActionPerformed
        if (this.validarPago()) {
            pagarTicket();
            setVisible(false);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Favor de ingresar el efectivo que pago el cliente", "Mensaje del Sistema", JOptionPane.WARNING_MESSAGE);
            this.jtxtPago.setFocusable(true);
            this.jtxtPago.requestFocus();
            this.jtxtPago.selectAll();
        }
    }//GEN-LAST:event_btnPagarVentaActionPerformed

    private boolean validarPago() {
        try {
            BigDecimal pagado = new BigDecimal(this.jtxtPago.getText());
            BigDecimal total_pagar = new BigDecimal(this.jtxtPago.getText());
            return !this.jtxtPago.getText().isEmpty()//el campo de pago no sea acio
                    && pagado.compareTo(BigDecimal.ZERO) > 0//pago es mayor a cero
                    && pagado.compareTo(total_pagar) >= 0;//pago mas o igual al total a pagar
        } catch (Exception e) {
        }
        return false;
    }

    private void pagarTicket() {
        Ticket ticket = this.getTicketListoPagar();
        try {
            this.ticketController.actualizarTckt(ticket);
        } catch (Exception e) {
            log.error("Error al tratar de pagar ticket", e);
        }
    }

    private Ticket getTicketListoPagar() {
        Ticket ticket = this.getTicket_pagar();
        ticket.setCambio(new BigDecimal(this.jtxtCambio.getText()));
        ticket.setPago(new BigDecimal(this.jtxtPago.getText()));
        ticket.setPagado(true);
        return ticket;
    }

