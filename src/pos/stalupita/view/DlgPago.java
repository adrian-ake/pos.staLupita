    private void calcularCambio() {
        this.jtxtCambio.setText("--");
        BigDecimal pago_ingresado = BigDecimal.ZERO;
        BigDecimal cambio = BigDecimal.ZERO;
        BigDecimal total_pagar = new BigDecimal(this.jtxtTotalVenta.getText());
        try {
            pago_ingresado = new BigDecimal(this.jtxtPago.getText());
        } catch (Exception e) {
        }
        if (pago_ingresado.compareTo(BigDecimal.ZERO) > 0) {
            cambio = pago_ingresado.subtract(total_pagar).setScale(2, RoundingMode.HALF_UP);
            if (cambio.compareTo(BigDecimal.ZERO) > 0) {
                this.jtxtCambio.setText(cambio.toString());
            } else if (cambio.compareTo(BigDecimal.ZERO) == 0) {
                this.jtxtCambio.setText(BigDecimal.ZERO.toString());
            }
        }
    }

